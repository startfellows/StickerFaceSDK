import UIKit

extension UIColor {
    
    open class var accentBrand: UIColor {
        return UIColor(hex: 0x45AEF5)
    }
    
    open class var defaultWhite: UIColor {
        return UIColor(hex: 0xFFFFFF)
    }
    
    open class var textPrimary: UIColor {
        return UIColor(hex: 0x151C29)
    }
    
    open class var textSecondary: UIColor {
        return UIColor(hex: 0x9FA5AE)
    }
    
}
